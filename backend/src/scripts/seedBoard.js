async function main() {
  console.log('게시판 더미 데이터 삽입 시작...');
  let postCount = 0, commentCount = 0;

  for (const p of posts) {
    const { comments = [], ...postData } = p;
    const post = await prisma.post.create({ data: postData });
    postCount++;

    for (const c of comments) {
      await prisma.comment.create({ data: { ...c, postId: post.id } });
      commentCount++;
    }
  }

  console.log(`완료! 게시글 ${postCount}개, 댓글 ${commentCount}개 삽입`);
}

main()
